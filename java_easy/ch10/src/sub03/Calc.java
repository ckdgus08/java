package sub03;

public class Calc {

	
	
	
	public int plus(int x, int y) {
		int z= x + y;
		return z;
	}
	public int minus(int x, int y) {
		int z= x - y;
		return z;
	}
	public int multi(int x, int y) {
		int z= x * y;
		return z;
	}
	public int div(int x, int y) throws Exception {
		int z= x / y;
		return z;
	}
}
